# Конфигурация gunicorn для ServiceDesk (Flask + Socket.IO на gevent).
#
# Запуск:
#   gunicorn -c deploy/gunicorn.conf.py run:app
#
# Почему именно так — см. deploy/ДИАГНОСТИКА.md, раздел «gunicorn».

# --- Куда слушать -----------------------------------------------------------
# За сайтом стоит Apache (reverse proxy), поэтому слушаем только localhost.
bind = "127.0.0.1:8000"

# --- Воркеры ----------------------------------------------------------------
# КРИТИЧЕСКИ ВАЖНО для Socket.IO: либо РОВНО ОДИН воркер, либо несколько
# воркеров + общая шина Redis (message_queue) + «липкие» сессии в Apache.
#
# Причина: при polling-/websocket-рукопожатии Socket.IO выдаёт клиенту sid
# (идентификатор сессии). Все последующие запросы с этим sid ДОЛЖНЫ попадать
# на тот же процесс. Если воркеров несколько и шины нет — запрос с sid
# прилетает на «чужой» воркер, который про эту сессию не знает, и отвечает
# 400 Bad Request. Именно это и сыпалось в консоль.
#
# Один gevent-воркер держит тысячи одновременных соединений, поэтому для
# вашей нагрузки workers = 1 — правильный и достаточный выбор.
workers = 1

# Класс воркера: gevent + нативная поддержка WebSocket.
# Требует пакет gevent-websocket (pip install gevent-websocket).
# Если оставить просто "gevent" — WebSocket-транспорт будет недоступен,
# и Socket.IO молча откатится на long-polling (работает, но медленнее и
# в консоли видны неуспешные попытки апгрейда до wss://).
worker_class = "geventwebsocket.gunicorn.workers.GeventWebSocketWorker"

# Сколько одновременных соединений тянет один gevent-воркер.
worker_connections = 2000

# --- Таймауты ---------------------------------------------------------------
# Для долгоживущих WebSocket/long-polling соединений нужен запас по времени.
timeout = 120
graceful_timeout = 30
keepalive = 5

# --- Логи -------------------------------------------------------------------
# capture_output + лог в "-" (stdout/stderr) => всё уходит в journald,
# и `journalctl -u servicedesk` показывает и доступ, и ошибки приложения.
capture_output = True
loglevel = "info"
accesslog = "-"
errorlog = "-"
